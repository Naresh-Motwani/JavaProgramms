package Collection_Programming;

public class TreeMap_Book_Example_i {

	int id, quantity;
	String name, author, publisher;
	public TreeMap_Book_Example_i(int id, int quantity, String name, String author, String publisher) {
		super();
		this.id = id;
		this.quantity = quantity;
		this.name = name;
		this.author = author;
		this.publisher = publisher;
	}
	@Override
	public String toString() {
		return "TreeMap_Book_Example_i [id=" + id + ", quantity=" + quantity + ", name=" + name + ", author=" + author
				+ ", publisher=" + publisher + "]";
	}
	
	

}
